package org.muse.ambrosia.api;

/**
 * Alert presents a rich-textual (html) alert to the user.
 */
public interface Alert extends Controller
{
	/**
	 * Set the instructions message.
	 * 
	 * @param selector
	 *        The message selector.
	 * @param references
	 *        one or more (or an array) of UiPropertyReferences to form the additional values in the formatted message.
	 */
	Alert setText(String selector, PropertyReference... references);
}
